package org.rscdaemon.server.entityhandling.defs.extras;

/**
 * The definition wrapper for ores
 */
public class ReqOreDef {

	/**
	 * The id of the ore
	 */
	public int oreId;
	/**
	 * The amount of the ore required
	 */
	public int amount;
	
	public int getId() {
		return oreId;
	}
	
	public int getAmount() {
		return amount;
	}
	
}
